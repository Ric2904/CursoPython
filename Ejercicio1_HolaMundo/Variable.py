# Manejo de variables

mi_entero = 10     # int ---> entero
mi_flotante = 5.7  # float --> punto decimal
mi_cadena = 'Juan' # str (string) --> cadena

# Mandar a imprimir (leer el valor de la variable)
print("El número entero es:")
print(mi_entero)

mi_entero = 20    # Modificar el valor
print(mi_entero)

print("El número flor es:")
print(mi_flotante)

print("El valor string (cadena) es:")
print(mi_cadena)




